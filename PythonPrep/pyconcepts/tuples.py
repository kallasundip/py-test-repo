""" --- A tuple is like except that it uses parentheses () instead of square brackets [] """
colours = ("red", "green", "blue")
print(colours[0])
print(colours[0:])
print(colours[0:2])

# colours[0] = "yellow" # can't modify the value

""" define a tuple """
number = ()
print(f"Type of Tuple:-  {type(number)}")
num = (3)
print(type(num))
